set<int> divisors(int n) {
  set<int> st;
  st.insert(1);

  // FIND DIVISORS
  for (int i = 2; i <= sqrt(n); i++) {
    if (n % i == 0) {
      st.insert(i);
      st.insert(n / i);
    }
  }

  return st;
}

auto solve(int tc) {
  int n;
  cin >> n;

  set<int> divs = divisors(n);
  int res = 0;
  for (auto it : divs) {
    if (it != n)
      res += it;
  }

  cout << res << endl;
}

